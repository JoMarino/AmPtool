      migrate = {};
      food = {};

    case 'Homo_sapiens'
      climate = {};
      ecozone = {};
      habitat = {};
      migrate = {};
      food = {};
      
    otherwise
      climate = {};
      ecozone = {};
      habitat = {};
      migrate = {};
      food = {};
  end

  if isempty(climate) && isempty(ecozone) && isempty(habitat) &&  isempty(migrate) && isempty(food)
